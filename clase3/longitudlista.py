# Lista vacía
# La longitud de una lista vacía
lista_vacia = [ ]
print("Longitud de una lista vacía: ", len(lista_vacia))

# Lista con enteros
# La longitud es igual al numero de elementos que tiene la lista
lista_enteros = [1, 2, 3, 4, 5]
print("Longitud de una lista de enteros es de: ", len(lista_enteros))

# Lista con cadenas
# La longitud es igual al número de elementos de la lista
lista_cadenas = ["manzana", "banana", "cereza"]
print("Longitud de una lista de cadenas es de: ", len(lista_cadenas))

# Lista mixta
# La longitud cuenta todos los elementos (sin importar el tipo de elemento)
lista_mixta = [1,"texto", 3.14, [1,2]]
print("Longitud de una lista mixta es de: ", len(lista_mixta))

# Longitud con elementos repetidos
# La longitud es el resultado de multiplicar el numero de elementos repetidos
lista_repetidos = [0] * 10
print("Longitud de una lista de numeros repetidos es de: ", len(lista_repetidos))

