# Ejemplos de operadores de asignación
# En este ejemplo la x comienza con un valor inicial de 10
# Conforme avanzamos x va tomando el ultimo valor para hacer la siguiente operación

# Asignación simple
x = 10
print(f"x = {x}")

# Suma y asignación
x += 5
print(f"x += 5: {x}")

# Resta y asignación
x -= 3
print(f"x -= 3: {x}") # Toma el resultado de la asignación anterior y le resta 15 - 3 = 12

# Multiplicación y asignación 
x *= 4
print(f"x *= 4: {x}")

# Division y asignación
x /= 2
print(f"x /= 2: {x}")

# Division entera y asignación
x //= 3
print(f"x //= 3: {x}")

# Modulo y asignación
x %= 7
print(f"x %= 7: {x}")

# Exponente y asignación
x **= 2
print(f"x **= 2: {x}")

